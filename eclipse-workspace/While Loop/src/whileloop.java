
public class whileloop {

	public static void main(String[] args) {
		
		int i = 1;
		
		while (i<=10) {
			System.out.println(i);
		
		i++;
		}
		
		int x = 1, sum = 0;
		
		while (x<=10) {
	
			sum = sum + x;
				
		x++;
			
		}
		System.out.println("Sum is: " + sum);
	}

}
